class While3
{
	public static void main(String[] args)
	{
		System.out.println();
		char str = 'A';
		while (str<='Z')
		{
			System.out.print(" | "+str+" |");
			str++;
		}

		System.out.println();
		System.out.println();
		
		char str2 = 'a';
		while (str2<='z')
		{
			System.out.print(" | "+str2+" |");
			str2++;
		} 
		System.out.println();
		
	}
}